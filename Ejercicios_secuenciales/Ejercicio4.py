# Creamos las variables de los números
numero1 = float(input("Dame un numero: "))
numero2 = float(input("Dame un numero: "))

# Calculamos la suma
suma = numero1 + numero2

# Calculamos la resta
resta = numero1 - numero2

# Calculamos la division
division = numero1 / numero2

# Calculamos la multiplicación
multiplicacion = numero1 * numero2

"Mostramos el resultado de las operaciones"
print("La suma es:", suma, "La resta es: ", resta, "La division es: ", division,
      "La multiplicación es: ", multiplicacion)
